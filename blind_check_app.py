# 위반 항목 감지 함수
def detect_violations(text):
    results = []
    for category, patterns in VIOLATION_RULES.items():
        for pattern in patterns:
            matches = re.findall(pattern, text)
            for match in matches:
                results.append((category, match))
    return results

